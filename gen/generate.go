// Code generation pipeline for the RAIL0 Go SDK.
//
// Run from the repository root with:
//
//	go run gen/generate.go
//
// Steps:
//  1. Read gen/openapi.json
//  2. Parse component schemas
//  3. Emit types_gen.go — do not hand-edit that file
//
// Add future generation steps (resource stubs, mock factories, etc.) here.
package main

import (
	"encoding/json"
	"fmt"
	"os"
	"path/filepath"
	"runtime"
	"slices"
	"strings"
	"text/template"
	"time"
	"unicode"
)

// ================================================================
//  Minimal OpenAPI 3.x types (only the subset we need)
// ================================================================

type Schema struct {
	Type        string             `json:"type"`
	Format      string             `json:"format"`
	Description string             `json:"description"`
	Properties  map[string]*Schema `json:"properties"`
	Required    []string           `json:"required"`
	Ref         string             `json:"$ref"`
	AllOf       []*Schema          `json:"allOf"`
	Enum        []string           `json:"enum"`
	Minimum     *float64           `json:"minimum"`
	Maximum     *float64           `json:"maximum"`
}

type OpenAPI struct {
	Components struct {
		Schemas map[string]*Schema `json:"schemas"`
	} `json:"components"`
}

// ================================================================
//  Type mapping
// ================================================================

// primitive schemas that map to Go type aliases
var primitiveAliases = map[string]string{
	"Address":       "string",
	"Bytes32":       "string",
	"Uint256String": "string",
}

// goType resolves an OpenAPI schema to a Go type expression.
func goType(s *Schema) string {
	// $ref → use the schema name directly
	if s.Ref != "" {
		parts := strings.Split(s.Ref, "/")
		return parts[len(parts)-1]
	}
	// allOf with a single $ref is used to attach a description to a ref — unwrap it.
	if len(s.AllOf) == 1 && s.AllOf[0].Ref != "" {
		parts := strings.Split(s.AllOf[0].Ref, "/")
		return parts[len(parts)-1]
	}
	switch s.Type {
	case "string":
		return "string"
	case "boolean":
		return "bool"
	case "integer":
		if s.Format == "int64" {
			return "int64"
		}
		return "int"
	case "number":
		return "float64"
	default:
		return "any"
	}
}

// exported converts a JSON property name (camelCase) to an exported Go name (PascalCase).
func exported(name string) string {
	if name == "" {
		return ""
	}
	r := []rune(name)
	r[0] = unicode.ToUpper(r[0])
	return string(r)
}

// ================================================================
//  Template data structures
// ================================================================

type Field struct {
	Name    string // exported Go name
	Type    string // Go type
	JSON    string // original JSON key
	Doc     string // inline doc comment (may be empty)
	Pointer bool   // true when the field is optional (not in required list)
}

type Struct struct {
	Name   string
	Doc    string
	Fields []Field
}

type Alias struct {
	Name string
	Base string
	Doc  string
}

type GenData struct {
	Timestamp string
	Aliases   []Alias
	Structs   []Struct
}

// ================================================================
//  Code template
// ================================================================

const goTemplate = `// Code generated by gen/generate.go on {{ .Timestamp }}; DO NOT EDIT.
// Source: gen/openapi.json
//
// To regenerate after changing the OpenAPI spec:
//   go run gen/generate.go

package rail0
{{ range .Aliases }}
// {{ .Name }} is a type alias for {{ .Base }}.{{ if .Doc }} {{ .Doc }}{{ end }}
type {{ .Name }} = {{ .Base }}
{{ end }}{{ range .Structs }}
// {{ .Name }}{{ if .Doc }} {{ .Doc }}{{ end }}
type {{ .Name }} struct {
{{ range .Fields }}	{{ if .Doc }}// {{ .Doc }}
	{{ end }}{{ .Name }} {{ .Type }} ` + "`" + `json:"{{ .JSON }}{{ if .Pointer }},omitempty{{ end }}"` + "`" + `
{{ end }}}
{{ end }}`

// ================================================================
//  Main
// ================================================================

func main() {
	// Locate gen/openapi.json relative to this file.
	_, thisFile, _, _ := runtime.Caller(0)
	genDir := filepath.Dir(thisFile)
	rootDir := filepath.Dir(genDir)

	schemaPath := filepath.Join(genDir, "openapi.json")
	outPath := filepath.Join(rootDir, "types_gen.go")

	fmt.Printf("Reading schema: %s\n", schemaPath)

	raw, err := os.ReadFile(schemaPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "error reading schema: %v\n", err)
		os.Exit(1)
	}

	var api OpenAPI
	if err := json.Unmarshal(raw, &api); err != nil {
		fmt.Fprintf(os.Stderr, "error parsing schema: %v\n", err)
		os.Exit(1)
	}

	data := buildData(api)

	tmpl, err := template.New("types").Parse(goTemplate)
	if err != nil {
		fmt.Fprintf(os.Stderr, "template parse error: %v\n", err)
		os.Exit(1)
	}

	f, err := os.Create(outPath)
	if err != nil {
		fmt.Fprintf(os.Stderr, "error creating output file: %v\n", err)
		os.Exit(1)
	}
	defer f.Close()

	if err := tmpl.Execute(f, data); err != nil {
		fmt.Fprintf(os.Stderr, "template execute error: %v\n", err)
		os.Exit(1)
	}

	fmt.Printf("Generated: %s\n", outPath)
	fmt.Println("Done.")
}

// buildData converts the OpenAPI schemas into template data.
func buildData(api OpenAPI) GenData {
	var aliases []Alias
	var structs []Struct

	// Process schemas in a stable order.
	names := make([]string, 0, len(api.Components.Schemas))
	for name := range api.Components.Schemas {
		names = append(names, name)
	}
	slices.Sort(names)

	for _, name := range names {
		s := api.Components.Schemas[name]

		if alias, ok := primitiveAliases[name]; ok {
			aliases = append(aliases, Alias{
				Name: name,
				Base: alias,
				Doc:  s.Description,
			})
			continue
		}

		if s.Type != "object" {
			// Skip non-object, non-primitive schemas (edge case).
			continue
		}

		st := Struct{Name: name, Doc: s.Description}

		// Build a set for O(1) required lookup.
		requiredSet := make(map[string]bool, len(s.Required))
		for _, r := range s.Required {
			requiredSet[r] = true
		}

		// Property order: required fields first, then optional, each group sorted.
		propNames := make([]string, 0, len(s.Properties))
		for pn := range s.Properties {
			propNames = append(propNames, pn)
		}
		slices.SortFunc(propNames, func(a, b string) int {
			aReq, bReq := requiredSet[a], requiredSet[b]
			if aReq != bReq {
				if aReq {
					return -1
				}
				return 1
			}
			return strings.Compare(a, b)
		})

		for _, propName := range propNames {
			prop := s.Properties[propName]
			optional := !requiredSet[propName]
			ft := goType(prop)
			// For optional pointer types (non-string primitives), use pointer.
			// Strings default to empty string which is fine for optional fields.
			usePointer := optional && ft != "string"

			fieldType := ft
			if usePointer {
				fieldType = "*" + ft
			}

			st.Fields = append(st.Fields, Field{
				Name:    exported(propName),
				Type:    fieldType,
				JSON:    propName,
				Doc:     prop.Description,
				Pointer: optional,
			})
		}

		structs = append(structs, st)
	}

	return GenData{
		Timestamp: time.Now().UTC().Format("2006-01-02 15:04:05 UTC"),
		Aliases:   aliases,
		Structs:   structs,
	}
}
